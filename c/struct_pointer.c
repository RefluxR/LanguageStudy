#include <stdio.h>
 
struct Test {
    int i;                      // 정수형 멤버

    // &("AB"의 첫 글자)처럼 취급됨 
    const char *g;              // 문자열 리터럴을 가르키는 포인터 멤버

    // const는 포인터가 가리키는 대상 데이터(여기서는 문자)를 변경할 수 없다는 제약 조건을 의미함
    // *g = 'Z'; 와 같은 코드는 컴파일 오류를 일으키거나 프로그램이 비정상 종료(Segmentation Fault)될 수 있음


};
 
int main() {
                        // 여기서 각 {}에 대응하는 값은 미리 설정한 int와 char 자료형(정확히는 문자열 리터럴)과 일치함
                        // 따라서 해당 구조체 변수 하나를 초기화하는 역할을 함
    struct Test test[] = {{1, "ABCD"}, {2, "DCBA"}, {3, "EBET"}}; 
    struct Test *p = &test[1]; 

                // p->g라는 문법은 사실 다음 두 가지 동작을 하나로 줄인 것

                    // 포인터 역참조 (Dereference): 포인터 p가 가리키는 실제 메모리 위치로 이동한다. (*p)
                    // 멤버 접근 (Access): 이동한 위치에 있는 구조체 변수에서 특정 멤버(g)에 접근한다. (.g)
                    // 따라서 p->g는 p가 가리키는 구조체 변수의 g 멤버에 접근하는 것과 동일함

                  //  "DC"    // 1                      --------  DC의 인덱스인 D에서 C로 한 칸(+1) 이동
    
    printf("%p\n",  (void*) p->g + (p->i - 1)); 



    printf("%s를 통한 연산값 *(p->g + (p->i - 1)) : ", "%C");
    printf("%c\n", *(p->g + (p->i - 1)));

    printf("%s를 통한 연산값 p->g + (p->i - 1) : ", "%s");
    printf("%s\n", p->g + (p->i - 1));


    printf("s로 값을 주소로 받나, c로 값을 문자로 받나 차이점은 뭐냐?\n");
    printf("결과적으로 둘 다 'C'가 출력되긴 하는데, 그 과정이 다름\n");
    printf("*(p->g + (p->i - 1)) 는 포인터 연산 후 그 주소에 있는 문자를 가져오는 것이고\n");
    printf("p->g + (p->i - 1) 는 포인터 연산 후 그 주소부터 시작하는 문자열 전체를 가져오는 것임\n");
    printf("따라서 각각 방식에 사용되는 이스케이프가 다름\n");

    // *(DC + 1)가 출력됨
    // p->g는 "DC"를 가리키고, p->i는 2이므로 (2 - 1) = 1이 되어 "DC" 문자열에서 한 칸 이동한 "C"가 출력됨

    // 언어에서 포인터(p->g)와 정수(1)의 덧셈(+)은 일반적인 숫자 덧셈이 아니다.
    // 일반적인 덧셈: 10 + 1 = 11
    // 포인터 덧셈: 주소 이동을 의미한다.

    /////////////////////// p->g는 문자열 "DC"의 시작 주소('D'의 위치)를 나타내는 포인터이고,
    // 여기에 1을 더하면 포인터가 가리키는 위치가 한 칸(문자 하나) 앞으로 이동하게 된다.

    // printf("그런데 왜 시이이ㅣ발 (p->i - 1)은 2-1처럼 일반 산술연산이 되느냐?\n");
    // printf("위에서 선언한 구조체를 한번 봐봐");

    // const char *g;  가 아니라 char g [10];  였다면?
    // p->g + (p->i - 1)  이 부분에서
    // p->g 는 char 배열의 시작 주소를 가리키는 포인터가 되고
    // (p->i - 1) 은 여전히 정수 1이므로
    // 포인터와 정수의 덧셈 연산이 성립하여
    // p->g + (p->i - 1) 는 char 배열의 시작 주소에서 한 칸(문자 하나) 앞으로 이동한 주소를 가리키게 된다
    // 따라서 결과적으로 동일하게 'C'가 출력된다

    /////////////// 그냥 쉽게말해서 포인터 산술 연산은 char는 +- 1 int는 +-4 이런식으로 증가 감소하는 거임

    return 0;
}


